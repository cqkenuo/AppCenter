    public static String toStringIfEmpty(String str) {
        if (isEmpty(str)) {
            str = "";
        }
        return str;
    }

    /**
     * 把字节数转换成字符串
     *
     * @param b
     * @return
     */
    public static String byteToString(long b) {
        if (b <= 0) {
            return 0 + "B";
        }
        if (b / (1024 * 1024) > 0) {
            DecimalFormat df = new DecimalFormat("0.00");
            return df.format(1d * b / (1024 * 1024)) + "MB";
        }
        if (b / 1024 > 0) {
            DecimalFormat df = new DecimalFormat("0.00");
            return df.format(1d * b / (1024)) + "KB";
        }
        return b + "B";

    }

    /**
     * 次数转换成字符串
     *
     * @param count
     * @return
     */
    public static String countToString(long count) {
        if (count / 100000000 > 0) {
            return new DecimalFormat("0.0").format(count * 1.0 / 100000000) + "亿";
        }
        if (count / 10000 > 0) {
            return count / 10000 + "万";
        }
        return String.valueOf(count);
    }

    /**
     * 得到一个字符串的长度,显示的长度,一个汉字或日韩文长度为2,英文字符长度为1
     *
     * @param s 需要得到长度的字符串
     * @return int 得到的字符串长度
     */
    public static int getStringLength(String s) {
        if (s == null)
            return 0;
        char[] c = s.toCharArray();
        int len = 0;
        for (int i = 0; i < c.length; i++) {
            len++;
            if (!isLetter(c[i])) {
                len++;
            }
        }
        return len;
    }

    public static boolean isLetter(char c) {
        int k = 0x80;
        return c / k == 0 ? true : false;
    }


    /** 解码Unicode
     *      * <p/>
     * <pre>
     * decodeUnicode(null)     =   null;
     * decodeUnicode("")       =   "";
     * capitalizeFirstLetter("\u0061\u0062")    =   "ab"
     * </pre>
     * @param originalStr
     * @return
     */
    public static String decodeUnicode(String originalStr) {
        if (TextUtils.isEmpty(originalStr)) {
            return originalStr;
        }
        char aChar;
        int len = originalStr.length();
        StringBuffer outBuffer = new StringBuffer(len);
        for (int x = 0; x < len; )
        {
            aChar = originalStr.charAt(x++);
            if (aChar == '\\')
            {
                aChar = originalStr.charAt(x++);
                if (aChar == 'u')
                {
                    // Read the xxxx
                    int value = 0;
                    for (int i = 0; i < 4; i++)
                    {
                        aChar = originalStr.charAt(x++);
                        switch (aChar)
                        {
                            case '0':
                            case '1':
                            case '2':
                            case '3':
                            case '4':
                            case '5':
                            case '6':
                            case '7':
                            case '8':
                            case '9':
                                value = (value << 4) + aChar - '0';
                                break;
                            case 'a':
                            case 'b':
                            case 'c':
                            case 'd':
                            case 'e':
                            case 'f':
                                value = (value << 4) + 10 + aChar - 'a';
                                break;
                            case 'A':
                            case 'B':
                            case 'C':
                            case 'D':
                            case 'E':
                            case 'F':
                                value = (value << 4) + 10 + aChar - 'A';
                                break;
                            default:
                                throw new IllegalArgumentException(
                                        "Malformed   \\uxxxx   encoding.");
                        }
                    }
                    outBuffer.append((char) value);
                } else
                {
                    if (aChar == 't')
                        aChar = '\t';
                    else if (aChar == 'r')
                        aChar = '\r';
                    else if (aChar == 'n')
                        aChar = '\n';
                    else if (aChar == 'f')
                        aChar = '\f';
                    outBuffer.append(aChar);
                }
            } else
                outBuffer.append(aChar);
        }
        return outBuffer.toString();
    }


